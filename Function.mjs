function square(num) {
    return num * num;
}

function inchesToMm(inches) {
    return inches * 25.4;
}

function squareRoot(num) {
    return Math.sqrt(num);
}

function cube(num) {
    return num * num * num;
}

function circleArea(radius) {
    return Math.PI * radius * radius
}

function greet(name) {
    return `Hello, ${name}`;
}